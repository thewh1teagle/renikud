#!/usr/bin/env python3
"""Export trained G2P checkpoint to C header files for ESP32 inference.

Usage:
    uv run tiny-renikud/scripts/export_weights.py <checkpoint_dir_or_safetensors> [--quantize]

Outputs:
    tiny-renikud/src/weights.h      — weight arrays (float32 or int8 + scales)
    tiny-renikud/src/model_config.h — vocab, label tables, letter constraints
"""

from __future__ import annotations

import argparse
import io
import sys
import unicodedata
from pathlib import Path

import numpy as np
import torch

# Allow imports from src/
REPO_ROOT = Path(__file__).resolve().parents[2]
sys.path.insert(0, str(REPO_ROOT / "src"))

from phonology import ID_TO_CONSONANT, ID_TO_VOWEL, HEBREW_LETTER_CONSONANT_IDS
from tokenization import build_vocab


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def load_state_dict(path: Path) -> dict[str, torch.Tensor]:
    if path.is_dir():
        path = path / "model.safetensors"
    if path.suffix == ".safetensors":
        from safetensors.torch import load_file
        return load_file(str(path))
    return torch.load(str(path), map_location="cpu")


def to_c_name(py_name: str) -> str:
    return py_name.replace(".", "_").replace("-", "_")


def write_float_array(buf: io.StringIO, name: str, data: np.ndarray) -> None:
    flat = data.flatten().astype(np.float32)
    buf.write(f"static const float {name}[{flat.size}] = {{\n")
    for i, v in enumerate(flat):
        buf.write(f"  {v:.8e}f,")
        if (i + 1) % 8 == 0:
            buf.write("\n")
    buf.write("\n};\n\n")


def write_int8_array(buf: io.StringIO, name: str, data: np.ndarray) -> tuple[float, float]:
    """Quantize to int8 symmetric, return (scale, zero_point=0)."""
    flat = data.flatten().astype(np.float32)
    amax = np.abs(flat).max()
    scale = float(amax) / 127.0 if amax > 0 else 1.0
    quantized = np.clip(np.round(flat / scale), -127, 127).astype(np.int8)
    buf.write(f"static const int8_t {name}[{quantized.size}] = {{\n")
    for i, v in enumerate(quantized):
        buf.write(f"  {int(v)},")
        if (i + 1) % 16 == 0:
            buf.write("\n")
    buf.write("\n};\n\n")
    return scale


def emit_weight(buf: io.StringIO, scales_buf: io.StringIO, name: str,
                tensor: torch.Tensor, quantize: bool) -> None:
    data = tensor.detach().float().numpy()
    if quantize and data.ndim >= 2:  # quantize matrices, keep biases as float
        scale = write_int8_array(buf, name, data)
        scales_buf.write(f"static const float {name}_scale = {scale:.8e}f;\n")
    else:
        write_float_array(buf, name, data)


# ---------------------------------------------------------------------------
# weights.h
# ---------------------------------------------------------------------------

def export_weights(state: dict[str, torch.Tensor], quantize: bool) -> str:
    buf = io.StringIO()
    scales_buf = io.StringIO()

    buf.write("#pragma once\n")
    buf.write("// Auto-generated by export_weights.py — do not edit\n\n")
    buf.write('#include <stdint.h>\n\n')

    def w(key: str, c_name: str) -> None:
        t = state[key]
        emit_weight(buf, scales_buf, c_name, t, quantize)

    # Embeddings
    w("encoder.embeddings.word_embeddings.weight",     "emb_word")
    w("encoder.embeddings.position_embeddings.weight", "emb_pos")
    w("encoder.embeddings.token_type_embeddings.weight","emb_type")
    w("encoder.embeddings.LayerNorm.weight",            "emb_ln_w")
    w("encoder.embeddings.LayerNorm.bias",              "emb_ln_b")

    # Transformer layers
    for i in range(3):
        p = f"encoder.encoder.layer.{i}"
        li = f"layer{i}"
        w(f"{p}.attention.self.query.weight",    f"{li}_q_w")
        w(f"{p}.attention.self.query.bias",      f"{li}_q_b")
        w(f"{p}.attention.self.key.weight",      f"{li}_k_w")
        w(f"{p}.attention.self.key.bias",        f"{li}_k_b")
        w(f"{p}.attention.self.value.weight",    f"{li}_v_w")
        w(f"{p}.attention.self.value.bias",      f"{li}_v_b")
        w(f"{p}.attention.output.dense.weight",  f"{li}_attn_out_w")
        w(f"{p}.attention.output.dense.bias",    f"{li}_attn_out_b")
        w(f"{p}.attention.output.LayerNorm.weight", f"{li}_attn_ln_w")
        w(f"{p}.attention.output.LayerNorm.bias",   f"{li}_attn_ln_b")
        w(f"{p}.intermediate.dense.weight",      f"{li}_ffn1_w")
        w(f"{p}.intermediate.dense.bias",        f"{li}_ffn1_b")
        w(f"{p}.output.dense.weight",            f"{li}_ffn2_w")
        w(f"{p}.output.dense.bias",              f"{li}_ffn2_b")
        w(f"{p}.output.LayerNorm.weight",        f"{li}_ffn_ln_w")
        w(f"{p}.output.LayerNorm.bias",          f"{li}_ffn_ln_b")

    # Classification heads (always float — small, and avoids scale complexity at output)
    w("consonant_head.weight", "head_consonant_w")
    w("consonant_head.bias",   "head_consonant_b")
    w("vowel_head.weight",     "head_vowel_w")
    w("vowel_head.bias",       "head_vowel_b")
    w("stress_head.weight",    "head_stress_w")
    w("stress_head.bias",      "head_stress_b")

    # Append scales after all arrays
    scales = scales_buf.getvalue()
    if scales:
        buf.write("// Per-tensor quantization scales\n")
        buf.write(scales)

    return buf.getvalue()



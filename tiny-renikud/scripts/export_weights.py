# model_config.h
# ---------------------------------------------------------------------------

def escape_utf8(s: str) -> str:
    """Encode a unicode string as a C string literal with hex escapes."""
    encoded = s.encode("utf-8")
    return "".join(f"\\x{b:02x}" for b in encoded)


def export_config() -> str:
    vocab = build_vocab()
    buf = io.StringIO()

    buf.write("#pragma once\n")
    buf.write("// Auto-generated by export_weights.py — do not edit\n\n")
    buf.write('#include <stdint.h>\n\n')

    # Model dimensions
    buf.write("#define HIDDEN_SIZE       96\n")
    buf.write("#define NUM_HEADS         4\n")
    buf.write("#define HEAD_DIM          24\n")
    buf.write("#define NUM_LAYERS        3\n")
    buf.write("#define INTERMEDIATE_SIZE 192\n")
    buf.write("#define VOCAB_SIZE        104\n")
    buf.write("#define MAX_SEQ_LEN       512\n")
    buf.write("#define NUM_CONSONANTS    25\n")
    buf.write("#define NUM_VOWELS        6\n")
    buf.write("#define NUM_STRESS        2\n")
    buf.write("#define CLS_TOKEN_ID      1\n")
    buf.write("#define SEP_TOKEN_ID      2\n")
    buf.write("#define PAD_TOKEN_ID      0\n")
    buf.write("#define UNK_TOKEN_ID      3\n\n")

    # Vocab: unicode codepoint → token id
    # Represented as a sorted array of (codepoint, id) pairs for binary search
    # Filter to single-char keys only (skip special tokens like [CLS])
    char_entries = [(ord(ch), tid) for ch, tid in vocab.items() if len(ch) == 1]
    char_entries.sort()
    buf.write(f"// {len(char_entries)} single-character vocab entries, sorted by codepoint\n")
    buf.write(f"#define VOCAB_CHAR_COUNT {len(char_entries)}\n")
    buf.write("static const uint32_t VOCAB_CODEPOINTS[VOCAB_CHAR_COUNT] = {\n  ")
    buf.write(", ".join(str(cp) for cp, _ in char_entries))
    buf.write("\n};\n")
    buf.write("static const uint8_t VOCAB_TOKEN_IDS[VOCAB_CHAR_COUNT] = {\n  ")
    buf.write(", ".join(str(tid) for _, tid in char_entries))
    buf.write("\n};\n\n")

    # Consonant labels (UTF-8 string table)
    buf.write("// IPA consonant labels indexed by consonant class id\n")
    buf.write("static const char* CONSONANT_LABELS[NUM_CONSONANTS] = {\n")
    for i in range(25):
        label = ID_TO_CONSONANT[i]
        buf.write(f'  "{escape_utf8(label)}",  // {i}: {label}\n')
    buf.write("};\n\n")

    # Vowel labels
    buf.write("static const char* VOWEL_LABELS[NUM_VOWELS] = {\n")
    for i in range(6):
        label = ID_TO_VOWEL[i]
        buf.write(f'  "{escape_utf8(label)}",  // {i}: {label}\n')
    buf.write("};\n\n")

    # Letter constraints: for each Hebrew letter, which consonant ids are allowed
    # Format: flat array + offset/length table
    hebrew_letters = [chr(cp) for cp, _ in char_entries if ord('א') <= cp <= ord('ת')]
    buf.write("// Per-letter consonant constraints\n")
    buf.write(f"#define NUM_HEBREW_LETTERS {len(hebrew_letters)}\n")

    flat_ids: list[int] = []
    offsets: list[tuple[int, int]] = []  # (offset, length)
    letter_codepoints: list[int] = []

    for letter in hebrew_letters:
        allowed = HEBREW_LETTER_CONSONANT_IDS.get(letter)
        letter_codepoints.append(ord(letter))
        if allowed is None:
            offsets.append((len(flat_ids), 0))
        else:
            offsets.append((len(flat_ids), len(allowed)))
            flat_ids.extend(allowed)

    buf.write("static const uint32_t HEBREW_LETTER_CODEPOINTS[NUM_HEBREW_LETTERS] = {\n  ")
    buf.write(", ".join(str(cp) for cp in letter_codepoints))
    buf.write("\n};\n")
    buf.write(f"static const uint8_t CONSTRAINT_IDS[{len(flat_ids)}] = {{\n  ")
    buf.write(", ".join(str(x) for x in flat_ids))
    buf.write("\n};\n")
    buf.write("static const uint8_t CONSTRAINT_OFFSET[NUM_HEBREW_LETTERS] = {\n  ")
    buf.write(", ".join(str(o) for o, _ in offsets))
    buf.write("\n};\n")
    buf.write("static const uint8_t CONSTRAINT_LEN[NUM_HEBREW_LETTERS] = {\n  ")
    buf.write(", ".join(str(l) for _, l in offsets))
    buf.write("\n};\n")

    return buf.getvalue()


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main() -> None:
    parser = argparse.ArgumentParser(description="Export G2P weights to C headers")
    parser.add_argument("checkpoint", type=Path, help="Path to checkpoint dir or model.safetensors")
    parser.add_argument("--quantize", action="store_true", help="Quantize weight matrices to int8")
    parser.add_argument("--out-dir", type=Path, default=REPO_ROOT / "tiny-renikud" / "src",
                        help="Output directory (default: tiny-renikud/src/)")
    args = parser.parse_args()

    out_dir: Path = args.out_dir
    out_dir.mkdir(parents=True, exist_ok=True)

    print(f"Loading checkpoint from {args.checkpoint} ...")
    state = load_state_dict(args.checkpoint)
    print(f"  {len(state)} tensors loaded")

    mode = "int8" if args.quantize else "float32"
    print(f"Exporting weights ({mode}) ...")
    weights_h = export_weights(state, args.quantize)
    weights_path = out_dir / "weights.h"
    weights_path.write_text(weights_h)
    size_kb = weights_path.stat().st_size / 1024
    print(f"  -> {weights_path}  ({size_kb:.1f} KB)")

    print("Exporting model config ...")
    config_h = export_config()
    config_path = out_dir / "model_config.h"
    config_path.write_text(config_h)
    print(f"  -> {config_path}")

    print("Done.")


if __name__ == "__main__":
    main()
